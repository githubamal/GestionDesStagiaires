package com.colaimo.model;

public class Service {
	Integer id;
	String nom;
	String chef;

	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}

	/**
	 * @param nom
	 *            the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}

	/**
	 * @return the chef
	 */
	public String getChef() {
		return chef;
	}

	/**
	 * @param chef
	 *            the chef to set
	 */
	public void setChef(String chef) {
		this.chef = chef;
	}

}
